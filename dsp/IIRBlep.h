	class HybridBlep
	{
	private:
		IIRBlep2::TwoPoleModal twoPoles[HybridBlepCoeffs::NumTwoPoles];
		IIRBlep2::OnePoleModal onePoles[HybridBlepCoeffs::NumOnePoles];
		float v = 0.0f;
		float firBuffer[HybridBlepCoeffs::FirSize] = { 0 };
		int firpos = 0;

	public:
		HybridBlep()
		{
			using namespace HybridBlepCoeffs;
			using namespace HybridBlepUtils;

			for (int i = 0; i < NumTwoPoles; ++i) {
				const float pre = twoPoleParams[i * 2 + 0];
				const float pim = twoPoleParams[i * 2 + 1];
				twoPoles[i].CalcPole(pre, pim);
			}

			for (int i = 0; i < NumOnePoles; ++i) {
				onePoles[i].CalcPole(onePoleParams[i]);
			}

			BuildTables();
			Reset();
		}

		void Add(float linear_gain, float tau, int mode = 1)
		{
			using namespace HybridBlepCoeffs;
			using namespace HybridBlepUtils;

			if (mode < BLIT_MODE || mode > BLAMP_MODE) return;

			if (tau < 0.0f) tau = 0.0f;
			if (tau >= 1.0f) tau = 0.9999999f;

			// IIR Blep
			const float fpos = tau * (float)(TableSize - 1);
			const int index1 = (int)fpos;
			const int index2 = index1 + 1;
			const float frac = fpos - (float)index1;

			const float(*twoPoleG1Table)[TableSize] = twoPoleBlitG1Table;
			const float(*twoPoleG2Table)[TableSize] = twoPoleBlitG2Table;
			const float(*onePoleG1Table)[TableSize] = onePoleBlitG1Table;

			const float (*firCoeffs)[FirSize] = firBlitCoeffs;

			if (mode == BLEP_MODE) {
				twoPoleG1Table = twoPoleBlepG1Table;
				twoPoleG2Table = twoPoleBlepG2Table;
				onePoleG1Table = onePoleBlepG1Table;
				firCoeffs = firBlepCoeffs;
			}
			else if (mode == BLAMP_MODE) {
				twoPoleG1Table = twoPoleBlampG1Table;
				twoPoleG2Table = twoPoleBlampG2Table;
				onePoleG1Table = onePoleBlampG1Table;
				firCoeffs = firBlampCoeffs;
			}

			for (int i = 0; i < NumTwoPoles; ++i) {
				const float g1 = LerpTable(twoPoleG1Table[i], index1, index2, frac) * linear_gain;
				const float g2 = LerpTable(twoPoleG2Table[i], index1, index2, frac) * linear_gain;
				twoPoles[i].InjectEvent(g1, g2);
			}

			for (int i = 0; i < NumOnePoles; ++i) {
				const float g1 = LerpTable(onePoleG1Table[i], index1, index2, frac) * linear_gain;
				onePoles[i].InjectEvent(g1);
			}

			// FIR Blep
			float firfpos = tau * (float)(FirTableSize - 1);
			int firTableIndex1 = firfpos;
			int firTableIndex2 = firTableIndex1 + 1;
			float firfrac = firfpos - (float)firTableIndex1;
			for (int i = 0; i < FirSize; ++i) {
				int index = (firpos + i) % FirSize;
				float v1 = firCoeffs[firTableIndex1][i];
				float v2 = firCoeffs[firTableIndex2][i];
				firBuffer[index] += linear_gain * (v1 + firfrac * (v2 - v1));
			}
		}

		void Step()
		{
			using namespace HybridBlepCoeffs;
			using namespace HybridBlepUtils;

			// IIR Blep
			float y = 0.0f;
			for (int i = 0; i < NumTwoPoles; ++i) {
				y += twoPoles[i].ProcessSample();
			}
			for (int i = 0; i < NumOnePoles; ++i) {
				y += onePoles[i].ProcessSample();
			}

			// FIR Blep
			float yfir = 0;
			yfir = firBuffer[firpos];
			firBuffer[firpos] = 0.0f;
			firpos = (firpos + 1) % FirSize;

			v = y + yfir;
		}

		float Get()
		{
			return v;
		}

		void Reset()
		{
			using namespace HybridBlepCoeffs;
			using namespace HybridBlepUtils;

			for (int i = 0; i < NumTwoPoles; ++i) {
				twoPoles[i].Reset();
			}
			for (int i = 0; i < NumOnePoles; ++i) {
				onePoles[i].Reset();
			}
			v = 0.0f;

			for (int i = 0; i < FirSize; ++i) {
				firBuffer[i] = 0.0f;
			}
			firpos = 0;
		}
	};

